def detect_role(text: str) -> str:
    """Определяет нужную роль по тексту. Возвращает ключ роли."""
    t = text.lower()
    scores = {}
    for role, keywords in ROLE_KEYWORDS.items():
        score = sum(1 for kw in keywords if kw in t)
        if score > 0:
            scores[role] = score
    if not scores:
        return "sensei"
    return max(scores, key=scores.get)


def get_role_addition(role: str) -> str:
    """Возвращает дополнительный промпт для роли."""
    return ROLE_PROMPTS.get(role, "")


