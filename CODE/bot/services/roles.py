# ──────────────────────────────────────────────────
# Хранение активной роли пользователя
# ──────────────────────────────────────────────────
_user_roles: dict = {}  # {user_id: role_key}
_user_role_locked: dict = {}  # {user_id: bool} — заблокировано командой /role


def set_role(user_id, role: str, locked: bool = False):
    """Устанавливает роль для пользователя."""
    _user_roles[str(user_id)] = role
    _user_role_locked[str(user_id)] = locked


def get_role(user_id) -> str:
    """Возвращает текущую роль пользователя."""
    return _user_roles.get(str(user_id), "sensei")


def is_locked(user_id) -> bool:
    """True если роль зафиксирована командой /role."""
    return _user_role_locked.get(str(user_id), False)


def auto_detect_and_set(user_id, text: str) -> str:
    """
    Автоматически определяет роль из текста (если не заблокирована).
    Возвращает ключ роли.
    """
    if is_locked(user_id):
        return get_role(user_id)
    role = detect_role(text)
    set_role(user_id, role, locked=False)
    return role


