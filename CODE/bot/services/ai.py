def ask_ai(user_id, text: str) -> str:
    """
    Главная функция общения с AI.

    Шаги:
      1. Сохранить вопрос пользователя в память.
      2. Собрать сообщения: системный промпт + профиль (долгая память) + история диалога.
      3. Спросить модель gpt-4.1-mini.
      4. Сохранить ответ в память и в документ Word.
      5. Извлечь новые факты о пользователе в профиль (фоново).
      6. Вернуть текст ответа.
    """
    save_memory(user_id, "user", text)

    # Определяем роль автоматически (или берём зафиксированную)
    from bot.services.roles import auto_detect_and_set, get_role_addition
    role = auto_detect_and_set(user_id, text)
    role_addition = get_role_addition(role)

    # Подгружаем профиль (долгая память)
    from bot.services.profile import get_profile, extract_facts_from_conversation
    profile_text = get_profile(user_id)

    # Собираем системный промпт + роль + профиль + актуальные долги из БД
    full_system = SYSTEM_PROMPT
    if role_addition:
        full_system += "\n\n" + role_addition
    if profile_text:
        full_system += "\n\n" + profile_text

    # Подставляем АКТУАЛЬНЫЕ долги из базы данных (не из захардкоженного текста)
    try:
        from bot.services.debts import get_debts_for_prompt
        full_system += "\n\n" + get_debts_for_prompt()
    except Exception as e:
        print(f"[AI] Не удалось подставить долги: {e}")

    messages = [
        {
            "role": "system",
            "content": full_system,
        }
    ]
    messages.extend(load_memory(user_id))

    def _call_gemini(msgs):
        """Вызов Gemini с авто-обрезкой истории при ошибке too many documents."""
        history = [m for m in msgs if m["role"] != "system"]
        system_msgs = [m for m in msgs if m["role"] == "system"]
        attempts = [len(history), max(len(history) // 2, 1), 2]
        last_exc = None
        for limit in attempts:
            trimmed = system_msgs + history[-limit:]
            try:
                return gemini_client.chat.completions.create(
                    model="gemini-2.0-flash",
                    messages=trimmed,
                    max_tokens=1500,
                )
            except Exception as e:
                err = str(e)
                if "too many images and documents" in err or "too many" in err.lower():
                    print(f"[AI] Gemini: слишком много сообщений ({limit}), обрезаю до {limit // 2 or 2}")
                    last_exc = e
                    continue
                raise  # другая ошибка — пробрасываем сразу
        raise last_exc  # все попытки исчерпаны

    # Стратегия: GPT-5 mini (основной) → Gemini 2.0 Flash (второй) → GPT-4.1 (крайний)
    # ВАЖНО: GPT-5 модели НЕ принимают max_tokens — только max_completion_tokens
    try:
        response = client.chat.completions.create(
            model="gpt-5-mini",
            messages=messages,
            max_completion_tokens=2000,
        )
        print("[AI] Ответил GPT-5 mini")
    except Exception as e:
        print(f"[AI] GPT-5 mini ошибка ({e}), переключаюсь на Gemini")
        if gemini_client:
            try:
                response = _call_gemini(messages)
                print("[AI] Ответил Gemini (резерв)")
            except Exception as e2:
                print(f"[AI] Gemini ошибка ({e2}), переключаюсь на GPT-4.1")
                response = client.chat.completions.create(
                    model="gpt-4.1",
                    messages=messages,
                )
                print("[AI] Ответил GPT-4.1 (крайний резерв)")
        else:
            response = client.chat.completions.create(
                model="gpt-4.1",
                messages=messages,
            )
            print("[AI] Ответил GPT-4.1 (крайний резерв)")

    ai_text = response.choices[0].message.content

    save_memory(user_id, "assistant", ai_text)
    save_docx(user_id, text, ai_text)

    # Извлекаем факты для долгой памяти (не блокирует ответ)
    import threading
    threading.Thread(
        target=extract_facts_from_conversation,
        args=(user_id, text, ai_text),
        daemon=True
    ).start()

    return ai_text
